    public static ArrayList<String> extractSentences(JTextArea textArea) {
        String text = textArea.getText().trim(); // Get the text from the JTextArea and remove leading/trailing whitespace
        String[] sentences = text.split("\\R"); // Split the text by line breaks (platform-independent regex)

        ArrayList<String> sentenceList = new ArrayList<>();
        for (String sentence : sentences) {
            sentenceList.add(sentence.trim()); // Add each sentence to the ArrayList (removing leading/trailing whitespace)
        }

        return sentenceList;
    }
    
