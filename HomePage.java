    public static ArrayList<String> convertStringToArrayList(String arrayListAsString) {
        // Remove the square brackets from the string representation
        String cleanString = arrayListAsString.substring(1, arrayListAsString.length() - 1);

        // Split the string by commas to get individual elements
        String[] elements = cleanString.split(", ");

        // Convert the array of strings to an ArrayList
        List<String> list = Arrays.asList(elements);
        return new ArrayList<>(list);
    }
    
