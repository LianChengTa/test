    public static ArrayList<Integer> getLatestSliderValues(ArrayList<JSlider> sliders) {
        ArrayList<Integer> sliderValues = new ArrayList<>();
        
        for (JSlider slider : sliders) {
            int value = slider.getValue();
            sliderValues.add(value);
        }
        
        return sliderValues;
    }
    
